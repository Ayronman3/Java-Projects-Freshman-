    double num1, num2, result;
    char opt;
    
    public double getnum(){
        if(opt == 0){
        num1 = Double.parseDouble(Display.getText());
        Display.setText("");
        return num1;
        }else{
            num2 = Double.parseDouble(Display.getText());
            return num2;
        }
    }
    public void compute(){
        switch(opt){
            case'+':
                result =  num1 + num2;
            break;
            case'-':
                result =  num1 - num2;
            break;
            case'X':
                result =  num1 * num2;
            break;
            case'รท':
                result =  num1 / num2;
            break;
            default:
                result = 0;
            
        }
    }
