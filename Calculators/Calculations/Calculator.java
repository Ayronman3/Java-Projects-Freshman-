    public double getNumber(){
        if(operations == 0){
            val1 = Double.parseDouble(Display.getText());
            Display.setText("");
            return val1;
        }else{
            val2 = Double.parseDouble(Display.getText());
            Display.setText("");
            return val2;
        }
    }
    public void compute(){
    
        
        if(operations == '+'){
            total = val1 + val2;
            
            
        }
        else if(operations == '-'){
            total = val1 - val2;
            
            
        }
        else if(operations == 'X'){
            total = val1 * val2;
           ;
         
        }
        else{
            total = val1 / val2;
            
            
        }
    }

